/*
C++에서는 try, catch, throw를 이용해서 예외처리를 해줌

try = 예외를 발견한다. catch = 예외를 잡는다. throw = 예외를 던진다.

try{ // 예외 발생 지역 }, catch(처리할 예외의 종류 명시){ // 예외처리 코드의 삽입 }

try블록 내에서 예외가 발생하면, 예외가 발생한 지점 이후의 나머지 try 영역은 건너 뜀

throw 절에 의해서 예외가 발생했는데 이를 처리하지 않으면 이러한 책임은 함수를 호출한 영역으로 넘어가게 됨

함수 내에서 함수를 호출한 영역으로 예외 데이터를 전달하면, 해당 함수는 더 이상 실행되지 않고 종료가 된다.
*/